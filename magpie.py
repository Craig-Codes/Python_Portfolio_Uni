number = int(input("Enter an integer between 1 and 7 inclusive: "))

if number == 1:
    print("One for sorrow")
elif number == 2:
    print("Two for joy")
elif number == 3:
    print("Three for a girl")
elif number == 4:
    print("Four for a boy")
elif number == 5:
    print("Five for silver")
elif number == 6:
    print("Six for gold")
elif number == 7:
    print("Seven for a secret never to be told")
else:
    print("Not a permitted number")
